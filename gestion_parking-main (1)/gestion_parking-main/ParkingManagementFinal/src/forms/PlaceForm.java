    private void btnajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnajouterActionPerformed
        // TODO add your handling code here:
        try{
            int numero = Integer.parseInt(jnumero.getText());
            String type = jtype.getSelectedItem().toString();
            String statut = jstatut.getSelectedItem().toString();
            Double tarif = Double.parseDouble(jtarif.getText());

            // creation d'objet Entite
            entites.Place p = new entites.Place(numero, type, statut, tarif);

            // la creation de l'objet Place
            if(pl.create(p)) {

                // msg de succes
                javax.swing.JOptionPane.showMessageDialog(this, "La Place numero "+ numero + "a ete ajoutee avec succes!"); 

                load(); //  recharge le tableau immédiatement
                //  vider les champs après l'ajout
                jnumero.setText("");
                jtarif.setText("");
            }
            else {
                 javax.swing.JOptionPane.showMessageDialog(this, "Erreur : Impossible d'ajouter la place. Vérifiez si le numéro existe déjà.", "Erreur", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(NumberFormatException e){
             javax.swing.JOptionPane.showMessageDialog(this, "Veuillez saisir des valeurs numeriques valides pour le numero et le tarif", "Format incorrect", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnajouterActionPerformed

    private void btnsupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsupprimerActionPerformed
        // TODO add your handling code here:
        int reponse = JOptionPane.showConfirmDialog(this, "Voulez vous bien suprimer cette place?");
        if (reponse == 0){
            pl.delete(numero);
            load();
        }
        
    }//GEN-LAST:event_btnsupprimerActionPerformed

    private void btnmodifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifierActionPerformed
        // TODO add your handling code here:
        // 1. On récupère les valeurs actuelles des champs (ce que l'utilisateur a modifié)
        int num = Integer.parseInt(jnumero.getText());
        String ty = jtype.getSelectedItem().toString();
        String st = jstatut.getSelectedItem().toString();
        double tr = Double.parseDouble(jtarif.getText());

        // 2. On crée l'objet 'p' (C'est ça le fameux "p" !)
        entites.Place p = new entites.Place(num, ty, st, tr);

        // 3. On envoie cet objet à la méthode update
        if(pl.update(p)){
            load(); // Recharge le tableau

            // Utilise showMessageDialog pour une simple info (Confirm c'est pour Oui/Non)
            JOptionPane.showMessageDialog(this, "Modification réussie !");

            // On déverrouille le numéro pour un futur ajout
            jnumero.setEditable(true);
        }
        else{
            JOptionPane.showMessageDialog(this, "Erreur lors de la modification.");
        }
        
    }//GEN-LAST:event_btnmodifierActionPerformed

