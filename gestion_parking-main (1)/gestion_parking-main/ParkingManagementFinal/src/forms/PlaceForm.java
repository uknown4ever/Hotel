    private void tableplaceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableplaceMouseClicked
        // TODO add your handling code here:
        int row = tableplace.getSelectedRow();
        if(row != 1){
         numero = Integer.parseInt(model.getValueAt(tableplace.getSelectedRow(), 0).toString());
         jnumero.setText(model.getValueAt(tableplace.getSelectedRow(),0).toString());
         jtype.setSelectedItem(model.getValueAt(tableplace.getSelectedRow(), 1).toString());
         jstatut.setSelectedItem(model.getValueAt(tableplace.getSelectedRow(), 2).toString());
         jtarif.setText(model.getValueAt(tableplace.getSelectedRow(),3).toString());
         
         jnumero.setEditable(false);
        }
        
    }//GEN-LAST:event_tableplaceMouseClicked


