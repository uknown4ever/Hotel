    void load() {
        model.setRowCount(0); // Vide le tableau
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss");

        for (Stationnement s : sdao.findAll()) {
            // Gérer l'affichage des valeurs nulles (si le véhicule est encore dans le parking)
            String dateS = (s.getDateSortie() == null) ? "En cours" : sdf.format(s.getDateSortie());
            String montant = (s.getMontant() == 0.0) ? "-" : String.valueOf(s.getMontant());

            model.addRow(new Object[]{
                s.getId(), s.getNumero(), s.getMatricule(),
                sdf.format(s.getDateEntree()), dateS, montant
            });
        }
    }

