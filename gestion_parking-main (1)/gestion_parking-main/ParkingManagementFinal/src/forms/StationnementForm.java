    private void btnSortieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortieActionPerformed
        // TODO add your handling code here:
        int row = tableStationnement.getSelectedRow();
        if (row != -1) {
            int id = Integer.parseInt(model.getValueAt(row, 0).toString());

            // Vérifier si le véhicule est bien encore dans le parking (dateSortie == null)
            if (!model.getValueAt(row, 4).toString().equals("En cours")) {
                JOptionPane.showMessageDialog(this, "Ce véhicule est déjà sorti.");
                return;
            }

            Stationnement s = new Stationnement();
            s.setId(id);

            // Le DAO calcule automatiquement le montant et la date
            sdao.update(s);

            load(); // Rafraîchit pour voir le résultat
            JOptionPane.showMessageDialog(this, "Sortie enregistrée !");
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un stationnement.");
        }
    }//GEN-LAST:event_btnSortieActionPerformed

    private void btnEntreeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntreeActionPerformed
        // TODO add your handling code here:
        try {
            int num = Integer.parseInt(txtNumeroPlace.getText());
            String mat = txtMatricule.getText();

            // Date actuelle générée automatiquement
            Stationnement s = new Stationnement(num, mat, new java.util.Date(), null, 0.0);

            if (sdao.create(s)) {
                load(); // Rafraîchit le tableau
                JOptionPane.showMessageDialog(this, "Véhicule entré !");
            } else {
                JOptionPane.showMessageDialog(this, "Erreur : Place occupée ou inexistante.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur de saisie : Vérifiez le numéro de place.");
        }
        
    }//GEN-LAST:event_btnEntreeActionPerformed


